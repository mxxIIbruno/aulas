"""
Python = Liguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

print(1234)

# Aspas Simples
print('Bruno')
print(1, '"Bruno Santos"')

# Aspas Duplas
print("Bruno")
print("'Bruno'")

# Escape
print(2, "Bruno \"Santos\"")

# r
print(r"Bruno \"Santos\"")
